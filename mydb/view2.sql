-- JOB_INFO 테이블 생성
-- SALARY는 $기준
CREATE TABLE JOB_INFO(
    JOB_ID      VARCHAR(10), -- 직업 아이디
    MIN_SALARY  NUMBER,      -- 최저급여
    MAX_SALARY  NUMBER       -- 최고급여    
);

